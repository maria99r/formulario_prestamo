    // comprueba si el campo esta vacio y lo marca en rojo. Devuelve false si esta vacio
    private boolean campoVacio(JTextField campo) {
        if (campo.getText().isEmpty()) {
            campo.setBackground(Color.red);
            return true;
        } else {
            campo.setBackground(Color.white);
        }
        return false;
    }

    // BOTON DE GUARDAR
    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed

        // comprueba si hay algun campo vacio
        if (campoVacio(nombre)
                || campoVacio(telefono)
                || campoVacio(dni)
                || campoVacio(isbn)
                || campoVacio(autor)
                || campoVacio(titulo)) {

        } else {
            // si están todos los campos, se crea un objeto registro con los datos
            Registro registroNuevo = new Registro(nombre.getText(), telefono.getText(), dni.getText(),
                    isbn.getText(), titulo.getText(), autor.getText(),
                    editorial.getSelectedItem().toString(), encuadernacion.getSelectedItem().toString(),
                    materias.getSelectedItem().toString(), topics.getSelectedItem().toString(),
                    prestamo.getValue().toString(), entrega.getValue().toString());

            // muestra mensaje de que se ha guardado y cierra la ventana
            JOptionPane.showMessageDialog(this,
                    "¡Datos guardados correctamente!",
                    "Guardar",
                    JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }

    }//GEN-LAST:event_guardarActionPerformed

    // BOTON DE SALIR
    private void salirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salirActionPerformed
        // si no ha rellenado ningun campo, sale directamente
        if (campoVacio(nombre)
                && campoVacio(telefono)
                && campoVacio(dni)
                && campoVacio(isbn)
                && campoVacio(autor)
                && campoVacio(titulo)) {
            dispose();
        } else {
            // si hay algun campo relleno, pregunta por confirmacion
            int respuesta = JOptionPane.showConfirmDialog(null, "Realmente desea salir?", "Confirmar salida", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (respuesta == JOptionPane.YES_OPTION) {
                dispose();
            }
        }

    }//GEN-LAST:event_salirActionPerformed

