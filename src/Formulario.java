    private void editorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editorialActionPerformed
        //Sólo se permitirá elegir el tipo de encuadernación en el caso de que para la 
        //Editorial se haya elegido "Círculo rojo" o "Vincens Vives". 
        //En cualquier otro caso, no se podrá seleccionar el tipo de encuadernación.
        //Entre los tipos de encuadernación podremos seleccionar "Rústica", "Tapa dura" o "Piel".
        
        // si la editorial es "Círculo rojo" (1) o "Vincens Vives" (2) se añaden el tipo de encuadernacion
        if (editorial.getSelectedIndex() == 1 | editorial.getSelectedIndex() == 2) {
            encuadernacion.setEnabled(true);
            encuadernacion.addItem("Rústica");
            encuadernacion.addItem("Tapa Dura");
            encuadernacion.addItem("Piel");
        } else {
            // en las otras editoriales se borran los elementos de la encuadernacion y se deshabilita
            encuadernacion.removeAllItems();
            encuadernacion.setEnabled(false);
        }
    }//GEN-LAST:event_editorialActionPerformed

    private void materiasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_materiasActionPerformed
//        Materias: Se podrá elegir entre las siguientes materias ”Alimentación-Nutrición”,” Medicina”, “Derecho” y "Música”. Así, dependiendo de la materia elegida, se podrá elegir una o más de una de entre las siguientes topics:
//En la materia “Alimentación-Nutrición”, se podrá elegir una o más de los siguientes topics: "Análisis de alimentos”, “Industria alimentaria”, “Nutrición y dietética”.
//En la materia “Medicina”,se podrá elegir una o más de una de entre los siguientes topics: “Neurología”, “Patología”, “Medicina general”.
//En la materia “Derecho”, se podrá elegir una o más de una de entre los siguientes topics: “Derecho privado”, “Derecho público”, “Derecho militar”.
//En la materia “Música”, se podrá elegir una o más de una de entre los siguientes topics: “Flamenco”, “Música clásica”, “Teoría e historia de la música”.

// segun la materia elegida, añade los topics (antes de añadir, borra para formatear los elementos)
        switch (materias.getSelectedIndex()) {
            case 0 -> {
                topics.removeAllItems();
                topics.addItem("Análisis de alimentos");
                topics.addItem("Industria alimentaria");
                topics.addItem("Nutrición y dietética");
                break;
            }
            case 1 -> {
                topics.removeAllItems();
                topics.addItem("Neurología");
                topics.addItem("Patología");
                topics.addItem("Medicina general");
                break;
            }
            case 2 -> {
                topics.removeAllItems();
                topics.addItem("Derecho privado");
                topics.addItem("Derecho público");
                topics.addItem("Derecho militar");
                break;
            }
            case 3 -> {
                topics.removeAllItems();
                topics.addItem("Flamenco");
                topics.addItem("Música clásica");
                topics.addItem("Teoría e historia de la música");
                break;
            }
        }

    }//GEN-LAST:event_materiasActionPerformed

